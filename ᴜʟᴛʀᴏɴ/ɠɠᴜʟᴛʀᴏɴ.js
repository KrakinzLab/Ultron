async function setWelcome(jid = null, text = null) {
  Greeting.findOrCreate({
    where: {
      chat: jid,
      greetingType: "welcome",
    },
    defaults: {
      chat: jid,
      switched: "ON",
      greetingType: "welcome",
      message: text,
    },
  });
}
