// ===============================================================================
async function checkSettings(jid = null, type) {
  var Msg = await Greeting.findAll({
    where: {
      chat: jid,
      greetingType: type,
    },
  });

  if (Msg.length < 1) {
    return false;
  } else {
    if (Msg[0].dataValues.switched === "ON") {
      return "ON";
    } else {
      return "OFF";
    }
  }
}
